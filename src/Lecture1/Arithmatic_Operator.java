package Lecture1;

public class Arithmatic_Operator {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        int c = a + b;
        System.out.println(c);
        c = a - b;
        System.out.println(c);
        c = b/a;
        System.out.println(c);
        c = a*b;
        System.out.println(c);
        c = a%b;
        System.out.println(c);
    }
}
